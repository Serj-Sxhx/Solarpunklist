export async function seedCommunities() {
  const existingCount = await storage.getCommunityCount();
  if (existingCount > 0) {
    console.log(`Database already has ${existingCount} communities, skipping seed.`);
    return;
  }

  console.log("Seeding database with community data...");

  for (const seed of SEED_COMMUNITIES) {
    const { tags, links, ...communityData } = seed;

    const community = await storage.createCommunity({
      ...communityData,
      lastResearchedAt: new Date(),
      lastRefreshedAt: new Date(),
    });

    if (tags && tags.length > 0) {
      await storage.addTags(community.id, tags);
    }

    if (links && links.length > 0) {
      await storage.addLinks(community.id, links);
    }

    console.log(`  Seeded: ${community.name}`);
  }

  console.log(`Seeded ${SEED_COMMUNITIES.length} communities.`);
}
